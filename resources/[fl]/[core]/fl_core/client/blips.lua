local AllBlips = {}
Citizen.CreateThread(function()
	Citizen.Wait(1000)
	for _, BlipInfo in pairs(Config.Map) do
		local blipMap = AddBlipForCoord(BlipInfo.pos)

		SetBlipSprite(blipMap, BlipInfo.id)
		SetBlipDisplay(blipMap, 4)
		if BlipInfo.scale then
			SetBlipScale(blipMap, BlipInfo.scale)
		else
			SetBlipScale(blipMap, 0.7)
		end
		SetBlipColour(blipMap, BlipInfo.color)
		SetBlipAsShortRange(blipMap, true)

		BeginTextCommandSetBlipName("STRING")
		AddTextComponentSubstringPlayerName(BlipInfo.name)
		EndTextCommandSetBlipName(blipMap)
		SetBlipPriority(blipMap, 5)

		table.insert(AllBlips, blipMap)
	end
end)

AddEventHandler('onResourceStop', function(resourceName)
	if GetCurrentResourceName() ~= resourceName then return end
	for _, blip in pairs(AllBlips) do
		RemoveBlip(blip)
	end
end)